package com.company.aashiq;

public class secondsInaYear {
    public static void main(String[] args) {
        int noOfDays = 365;
        int noOfhours = 24 * noOfDays;
        int noOfSeconds = noOfhours * 60 * 60;
        System.out.println("no of seconds in a year is: " +noOfSeconds);
    }
}
